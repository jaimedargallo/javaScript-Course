// OPERADORES DE ASIGNACIÓN

var numero;

// = Asignar un valor

numero = 5;

// += Asignar un valor sumándolo con el valor actual

numero += 3

// -= Asignar un valor restandolo con el valor actual

numero -= 3

// *= Asignar un valor multiplicando el valor actual

numero -= 3

// -= Asignar un valor dividiendo  el valor actual

numero /= 3